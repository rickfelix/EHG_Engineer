/**
 * USER_STORY_COVERAGE Semantic Gate
 * Part of SD-LEO-FEAT-SEMANTIC-VALIDATION-GATES-002 (Gate 8)
 *
 * Verifies that all user stories have been addressed with validated acceptance criteria.
 * Checks user_stories table for validation_status.
 *
 * Phase: EXEC-TO-PLAN (primary), PLAN-TO-LEAD (verification pass)
 */

import {
  getGateApplicability,
  computeConfidence,
  isAutoGenerated,
  buildSemanticResult,
  buildSkipResult
} from '../../../validation/semantic-gate-utils.js';

const GATE_NAME = 'USER_STORY_COVERAGE';

export function createUserStoryCoverageGate(supabase) {
  return {
    name: GATE_NAME,
    validator: async (ctx) => {
      console.log('\n📖 SEMANTIC GATE: User Story Coverage');
      console.log('-'.repeat(50));

      const sdType = ctx.sd?.sd_type || ctx.sdType || 'feature';
      const sdId = ctx.sd?.id || ctx.sdId;

      const { applicable, level } = getGateApplicability(GATE_NAME, sdType);
      if (!applicable) {
        console.log(`   ℹ️  Skipped for SD type: ${sdType}`);
        return buildSkipResult(GATE_NAME, sdType);
      }

      if (!supabase || !sdId) {
        return buildSemanticResult({
          passed: true, score: 50, confidence: 0.3,
          warnings: ['Cannot validate user stories — missing context']
        });
      }

      try {
        const { data: stories, error } = await supabase
          .from('user_stories')
          .select('id, story_key, title, status, validation_status, acceptance_criteria, created_by, metadata')
          .eq('sd_id', sdId);

        if (error) {
          console.log(`   ⚠️  Database error: ${error.message}`);
          return buildSemanticResult({
            passed: true, score: 50, confidence: 0.3,
            warnings: [`Database error: ${error.message}`]
          });
        }

        if (!stories || stories.length === 0) {
          console.log('   ⚠️  No user stories found');
          return buildSemanticResult({
            passed: level === 'OPT',
            score: level === 'OPT' ? 50 : 0,
            confidence: 0.7,
            issues: level === 'REQ' ? ['No user stories found for this SD'] : [],
            warnings: level === 'OPT' ? ['No user stories — optional for this SD type'] : [],
            remediation: 'Add user stories to user_stories table with sd_id'
          });
        }

        let covered = 0;
        let autoGenCount = 0;
        const uncovered = [];

        for (const story of stories) {
          if (isAutoGenerated(story)) {
            autoGenCount++;
            uncovered.push(`${story.story_key}: ${story.title} (auto-generated)`);
            continue;
          }

          const isValidated = story.validation_status === 'validated' || story.validation_status === 'in_progress';
          const isCompleted = story.status === 'completed' || story.status === 'testing';
          const hasAcceptanceCriteria = story.acceptance_criteria && story.acceptance_criteria.length > 0;

          if ((isValidated || isCompleted) && hasAcceptanceCriteria) {
            covered++;
          } else {
            const reasons = [];
            if (!isValidated && !isCompleted) reasons.push(`status: ${story.status}`);
            if (!hasAcceptanceCriteria) reasons.push('no acceptance criteria');
            uncovered.push(`${story.story_key}: ${story.title} (${reasons.join(', ')})`);
          }
        }

        const effectiveTotal = stories.length - autoGenCount;
        const score = effectiveTotal > 0 ? Math.round((covered / effectiveTotal) * 100) : 0;
        const confidence = computeConfidence({ dataPoints: stories.length, expectedPoints: Math.max(stories.length, 3) });
        const passed = level === 'OPT' ? true : score >= 80;

        console.log(`   📊 Stories: ${covered}/${effectiveTotal} covered (${autoGenCount} auto-generated excluded)`);
        console.log(`   ${passed ? '✅' : '❌'} Score: ${score}/100 | Confidence: ${confidence}`);

        if (uncovered.length > 0) {
          console.log('   Uncovered:');
          uncovered.slice(0, 5).forEach(u => console.log(`      - ${u}`));
          if (uncovered.length > 5) console.log(`      ... and ${uncovered.length - 5} more`);
        }

        return buildSemanticResult({
          passed,
          score,
          confidence,
          issues: !passed ? [`${uncovered.length} user story(ies) not covered`] : [],
          warnings: level === 'OPT' && score < 80 ? [`Low story coverage (${score}%) — advisory`] : [],
          details: {
            total: stories.length, covered, autoGenerated: autoGenCount,
            uncovered: uncovered.slice(0, 10), coverageRate: score
          },
          remediation: !passed ? `Complete user stories: ${uncovered.slice(0, 3).join('; ')}` : undefined
        });
      } catch (err) {
        console.log(`   ⚠️  Error: ${err.message}`);
        return buildSemanticResult({
          passed: true, score: 50, confidence: 0.3,
          warnings: [`User story coverage error: ${err.message}`]
        });
      }
    },
    required: true,
    weight: 1.0
  };
}
