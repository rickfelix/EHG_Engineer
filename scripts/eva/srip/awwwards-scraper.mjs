// ---------------------------------------------------------------------------

async function main() {
  const isDryRun = process.argv.includes('--dry-run');

  console.log('=== Awwwards Curated Design Reference Seeder ===');
  console.log(`Total references: ${CURATED_REFERENCES.length}`);
  console.log(`Dry run: ${isDryRun}\n`);

  // Print archetype breakdown
  const archetypeCounts = {};
  for (const ref of CURATED_REFERENCES) {
    archetypeCounts[ref.archetype_category] = (archetypeCounts[ref.archetype_category] || 0) + 1;
  }
  console.log('Archetype breakdown:');
  for (const [archetype, count] of Object.entries(archetypeCounts).sort()) {
    console.log(`  ${archetype}: ${count}`);
  }
  console.log('');

  if (isDryRun) {
    console.log('--- DRY RUN: Printing first 3 entries per archetype ---\n');
    const seen = {};
    for (const ref of CURATED_REFERENCES) {
      if (!seen[ref.archetype_category]) seen[ref.archetype_category] = 0;
      if (seen[ref.archetype_category] < 3) {
        console.log(`[${ref.archetype_category}] ${ref.site_name} (${ref.url})`);
        console.log(`  Scores: D=${ref.design_score} U=${ref.usability_score} C=${ref.creativity_score} Co=${ref.content_score}`);
        console.log(`  Agency: ${ref.agency_name} | Country: ${ref.country} | Tech: ${ref.tech_stack}`);
        console.log('');
        seen[ref.archetype_category]++;
      }
    }
    console.log('Dry run complete. No data was inserted.');
    return;
  }

  // Dynamic import of the service to allow --dry-run without Supabase credentials
  const { bulkInsertReferences } = await import('../../../lib/eva/services/design-reference-library.js');

  console.log('Connecting to Supabase and inserting references...');
  try {
    const inserted = await bulkInsertReferences(CURATED_REFERENCES);
    console.log(`Successfully upserted ${inserted.length} design references.`);
  } catch (err) {
    console.error(`Failed to insert references: ${err.message}`);
    process.exit(1);
  }
}

if (isMainModule(import.meta.url)) {
  main();
}
