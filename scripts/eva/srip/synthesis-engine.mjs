/**
 * Build the DESIGN_SYSTEM section from DNA design tokens + brand colors/typography.
 */
function buildDesignSystemSection(dnaJson, brandAnswers) {
  const tokens = dnaJson.design_tokens || {};
  const colors = tokens.colors || {};
  const typography = tokens.typography || {};

  const section = {
    title: 'DESIGN_SYSTEM',
    instructions: 'Apply these design tokens as the foundation for all visual styling.',
    colors: {
      primary: brandAnswers.color_primary || colors.primary || '#000000',
      secondary: brandAnswers.color_secondary || colors.secondary || '#666666',
      accent: colors.accent || null,
      background: colors.background || '#ffffff',
      text: colors.text || '#333333',
      additional: colors.additional || [],
    },
    typography: {
      font_family: brandAnswers.typography_preference || typography.font_family || 'system-ui, sans-serif',
      heading_font: typography.heading_font || null,
      size_scale: typography.size_scale || ['14px', '16px', '20px', '24px', '32px', '48px'],
      weights: typography.weights || [400, 600, 700],
      line_heights: typography.line_heights || [1.2, 1.5, 1.6],
    },
    spacing: tokens.spacing || ['4px', '8px', '16px', '24px', '32px', '48px'],
    border_radius: tokens.border_radius || ['4px', '8px'],
    shadows: tokens.shadows || [],
  };

  return section;
}

