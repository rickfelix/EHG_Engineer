/**
 * Generate a synthesis prompt from Site DNA + Brand Interview data.
 *
 * @param {object} params
 * @param {string} params.siteDnaId - UUID of the srip_site_dna record
 * @param {string} params.brandInterviewId - UUID of the srip_brand_interviews record
 * @param {object} [params.supabase] - Optional Supabase client
 * @returns {object|null} The stored synthesis prompt record, or null on failure
 */
export async function generateSynthesisPrompt({ siteDnaId, brandInterviewId, supabase }) {
  if (!supabase) {
    supabase = createSupabaseServiceClient();
  }

  console.log('\n   Synthesis Engine');
  console.log(`   Site DNA: ${siteDnaId}`);
  console.log(`   Brand Interview: ${brandInterviewId}`);

  // Load Site DNA
  const { data: siteDna, error: dnaError } = await supabase
    .from('srip_site_dna')
    .select('id, venture_id, dna_json, reference_url, quality_score')
    .eq('id', siteDnaId)
    .single();

  if (dnaError || !siteDna) {
    console.error(`   Site DNA not found: ${dnaError?.message || 'no data'}`);
    return null;
  }

  // Load Brand Interview
  const { data: interview, error: interviewError } = await supabase
    .from('srip_brand_interviews')
    .select('id, venture_id, answers, pre_populated_count, manual_input_count, status')
    .eq('id', brandInterviewId)
    .single();

  if (interviewError || !interview) {
    console.error(`   Brand interview not found: ${interviewError?.message || 'no data'}`);
    return null;
  }

  const dnaJson = siteDna.dna_json || {};
  const brandAnswers = interview.answers || {};

  console.log(`   DNA quality: ${siteDna.quality_score || 'N/A'}/100`);
  console.log(`   Brand completeness: ${interview.pre_populated_count}/${interview.pre_populated_count + interview.manual_input_count} answered`);

  // Build prompt sections
  const sections = [
    buildDesignSystemSection(dnaJson, brandAnswers),
    buildLayoutSection(dnaJson, brandAnswers),
    buildComponentsSection(dnaJson, brandAnswers),
    buildBrandVoiceSection(brandAnswers),
    buildTechnicalSection(dnaJson),
  ];

  // Assemble full prompt text
  const promptText = assemblePromptText(sections);

  // Estimate token count (rough: ~4 chars per token)
  const tokenCount = Math.ceil(promptText.length / 4);

  // Calculate fidelity target based on data completeness
  const dnaStepCount = Object.keys(dnaJson).filter(k => !dnaJson[k]?.error).length;
  const answeredCount = Object.values(brandAnswers).filter(v => v != null).length;
  const totalInputs = 6 + 12; // 6 DNA steps + 12 brand questions
  const fidelityTarget = Math.round(((dnaStepCount + answeredCount) / totalInputs) * 100);

  console.log(`\n   Prompt length: ${promptText.length} chars (~${tokenCount} tokens)`);
  console.log(`   Fidelity target: ${fidelityTarget}%`);
  console.log(`   Sections: ${sections.map(s => s.title).join(', ')}`);

  // Store in database
  const promptRecord = {
    venture_id: siteDna.venture_id || interview.venture_id || null,
    site_dna_id: siteDnaId,
    brand_interview_id: brandInterviewId,
    prompt_text: promptText,
    fidelity_target: fidelityTarget,
    version: 1,
    token_count: tokenCount,
    status: 'active',
    created_by: 'SRIP_SYNTHESIS_ENGINE',
  };

  const { data, error } = await supabase
    .from('srip_synthesis_prompts')
    .insert(promptRecord)
    .select('id, version, token_count, fidelity_target, status');

  if (error) {
    console.error(`   DB insert failed: ${error.message}`);
    return null;
  }

  const result = data[0];
  console.log(`\n   Synthesis prompt stored: ${result.id}`);
  console.log(`   Version: ${result.version} | Tokens: ${result.token_count} | Fidelity: ${result.fidelity_target}%`);

  return result;
}

