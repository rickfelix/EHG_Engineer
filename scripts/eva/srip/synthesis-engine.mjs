/**
 * Assemble the full synthesis prompt text from all sections.
 */
function assemblePromptText(sections) {
  const lines = [
    '# SITE REPLICATION PROMPT',
    '# Generated by SRIP Synthesis Engine',
    `# Generated at: ${new Date().toISOString()}`,
    '',
    'Replicate the reference site using the specifications below.',
    'Each section provides constraints and design decisions to follow precisely.',
    '',
  ];

  for (const section of sections) {
    lines.push(`## ${section.title}`);
    lines.push(`> ${section.instructions}`);
    lines.push('');

    // Serialize section data (excluding title and instructions)
    const data = { ...section };
    delete data.title;
    delete data.instructions;

    for (const [key, value] of Object.entries(data)) {
      if (value === null || value === undefined) continue;

      if (Array.isArray(value)) {
        if (value.length === 0) continue;
        lines.push(`### ${key.replace(/_/g, ' ').toUpperCase()}`);
        if (typeof value[0] === 'object') {
          for (const item of value) {
            const itemStr = Object.entries(item)
              .map(([k, v]) => `${k}: ${typeof v === 'object' ? JSON.stringify(v) : v}`)
              .join(' | ');
            lines.push(`- ${itemStr}`);
          }
        } else {
          lines.push(value.map(v => `- ${v}`).join('\n'));
        }
        lines.push('');
      } else if (typeof value === 'object') {
        lines.push(`### ${key.replace(/_/g, ' ').toUpperCase()}`);
        for (const [subKey, subVal] of Object.entries(value)) {
          if (subVal === null || subVal === undefined) continue;
          if (Array.isArray(subVal)) {
            lines.push(`- ${subKey}: ${subVal.join(', ')}`);
          } else {
            lines.push(`- ${subKey}: ${subVal}`);
          }
        }
        lines.push('');
      } else {
        lines.push(`- **${key.replace(/_/g, ' ')}**: ${value}`);
      }
    }

    lines.push('---');
    lines.push('');
  }

  return lines.join('\n');
}

