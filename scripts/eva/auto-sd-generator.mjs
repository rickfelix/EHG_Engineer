/**
 * EVA Auto-SD Generator — Phase 3 Recommendation-to-SD Pipeline
 * SD: SD-LEO-INFRA-CONSULTANT-AGENT-PHASE-004
 *
 * Converts accepted recommendations with action_type='create_sd' into
 * draft Strategic Directives in strategic_directives_v2.
 *
 * Deduplicates by checking existing SD titles for similarity.
 * Links generated SDs back to the source recommendation.
 */

import { createSupabaseServiceClient } from '../../lib/supabase-client.js';
import dotenv from 'dotenv';

dotenv.config();

const supabase = createSupabaseServiceClient();

// ─── Fetch Accepted Recommendations ───────────────────────

async function fetchAcceptedCreateSD() {
  const { data, error } = await supabase
    .from('eva_consultant_recommendations')
    .select('id, title, description, application_domain, priority_score, trend_id, chairman_feedback')
    .eq('status', 'accepted')
    .eq('action_type', 'create_sd')
    .order('priority_score', { ascending: false });

  if (error) {
    console.error('Error fetching recommendations:', error.message);
    return [];
  }
  return data || [];
}

// ─── Deduplication ────────────────────────────────────────

async function findExistingSDs() {
  const { data, error } = await supabase
    .from('strategic_directives_v2')
    .select('sd_key, title')
    .in('status', ['draft', 'in_progress', 'ready', 'completed']);

  if (error) {
    console.error('Error fetching existing SDs:', error.message);
    return [];
  }
  return data || [];
}

function isDuplicate(recTitle, existingSDs) {
  const normalizedTitle = recTitle.toLowerCase().trim();
  return existingSDs.some(sd => {
    const sdTitle = (sd.title || '').toLowerCase().trim();
    // Exact match or high substring overlap
    if (sdTitle === normalizedTitle) return true;
    if (sdTitle.includes(normalizedTitle) || normalizedTitle.includes(sdTitle)) return true;
    // Word overlap > 70%
    const recWords = new Set(normalizedTitle.split(/\s+/));
    const sdWords = new Set(sdTitle.split(/\s+/));
    const overlap = [...recWords].filter(w => sdWords.has(w)).length;
    return overlap / Math.max(recWords.size, sdWords.size) > 0.7;
  });
}

// ─── SD Creation ──────────────────────────────────────────

function mapDomainToType(domain) {
  const typeMap = {
    ehg_engineer: 'infrastructure',
    ehg_app: 'feature',
    new_venture: 'feature'
  };
  return typeMap[domain] || 'infrastructure';
}

function mapDomainToCategory(domain) {
  const catMap = {
    ehg_engineer: 'infrastructure',
    ehg_app: 'feature',
    new_venture: 'feature'
  };
  return catMap[domain] || 'EVA';
}

function generateSDKey(title) {
  const words = title.replace(/[^a-zA-Z0-9\s]/g, '').split(/\s+/).slice(0, 4);
  const key = words.map(w => w.toUpperCase()).join('-');
  return `SD-EVA-AUTO-${key}`;
}

async function createDraftSD(rec) {
  const sdKey = generateSDKey(rec.title);
  const sdType = mapDomainToType(rec.application_domain);
  const category = mapDomainToCategory(rec.application_domain);

  const fullDesc = rec.description + (rec.chairman_feedback ? `\n\nChairman notes: ${rec.chairman_feedback}` : '');

  const sd = {
    id: sdKey,
    sd_key: sdKey,
    title: rec.title,
    description: fullDesc,
    sd_type: sdType,
    category,
    status: 'draft',
    current_phase: 'LEAD',
    progress: 0,
    priority: rec.priority_score >= 0.9 ? 'high' : rec.priority_score >= 0.7 ? 'medium' : 'low',
    target_application: rec.application_domain === 'ehg_app' ? 'EHG' : 'EHG_Engineer',
    strategic_objectives: [rec.description],
    success_criteria: ['Implementation matches recommendation scope'],
    key_changes: [`Auto-generated from EVA Consultant recommendation: ${rec.id}`],
    rationale: `Auto-generated by EVA Consultant from recommendation: ${rec.title}`,
    strategic_intent: rec.description,
    scope: rec.description,
    key_principles: ['Follow LEO Protocol for all changes', 'Ensure backward compatibility'],
    implementation_guidelines: ['Review recommendation context before implementation'],
    success_metrics: [{ metric: 'Implementation complete', target: 'Matches recommendation scope' }],
    risks: [],
    stakeholders: [],
    created_by: 'eva-consultant',
    metadata: {
      created_via: 'eva-auto-sd-generator',
      source_recommendation_id: rec.id,
      created_at: new Date().toISOString()
    }
  };

  const { data, error } = await supabase
    .from('strategic_directives_v2')
    .insert(sd)
    .select('id, sd_key, title, status');

  if (error) {
    // May fail if sd_key already exists
    if (error.message.includes('duplicate')) {
      console.log(`   ⏭️  SD "${sdKey}" already exists — skipping`);
      return null;
    }
    console.error(`   Error creating SD: ${error.message}`);
    return null;
  }

  return data?.[0] || null;
}

// ─── Main ──────────────────────────────────────────────────

async function main() {
  console.log('╔══════════════════════════════════════════════════════════╗');
  console.log('║  EVA Auto-SD Generator — Phase 3                      ║');
  console.log('╚══════════════════════════════════════════════════════════╝');
  console.log();

  // 1. Fetch accepted create_sd recommendations
  console.log('📥 Fetching accepted create_sd recommendations...');
  const recs = await fetchAcceptedCreateSD();
  console.log(`   Found ${recs.length} accepted create_sd recommendations`);

  if (recs.length === 0) {
    console.log('\n⚠️  No accepted create_sd recommendations found.');
    console.log('   Accept recommendations via: node scripts/eva/recommendation-feedback.mjs accept <id>');
    return;
  }

  // 2. Load existing SDs for dedup
  console.log('\n🔍 Checking for duplicates...');
  const existingSDs = await findExistingSDs();
  console.log(`   ${existingSDs.length} existing SDs loaded for dedup`);

  // 3. Create SDs
  console.log('\n🏗️  Creating draft SDs...');
  let created = 0;
  let skipped = 0;

  for (const rec of recs) {
    if (isDuplicate(rec.title, existingSDs)) {
      console.log(`   ⏭️  "${rec.title}" — duplicate of existing SD, skipping`);
      skipped++;
      continue;
    }

    const sd = await createDraftSD(rec);
    if (sd) {
      console.log(`   ✅ Created: ${sd.sd_key} — ${sd.title}`);
      created++;
      // Add to existing SDs to prevent self-duplicates within this run
      existingSDs.push({ sd_key: sd.sd_key, title: sd.title });
    }
  }

  // 4. Summary
  console.log('\n═══════════════════════════════════════════════════════════');
  console.log(`  Created: ${created} draft SD(s)`);
  console.log(`  Skipped: ${skipped} (duplicates)`);
  console.log(`  Total processed: ${recs.length}`);
  if (created > 0) {
    console.log('\n  Next: Run npm run sd:next to see new SDs in the queue');
  }
  console.log('═══════════════════════════════════════════════════════════');
  console.log('\n✅ Auto-SD generation complete.');
}

main().catch(err => {
  console.error('Fatal error:', err);
  process.exit(1);
});
