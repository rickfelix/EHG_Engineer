import { writeFileSync } from 'fs';
import { join } from 'path';

/**
 * Generate a venture-specific CLAUDE.md file.
 *
 * @param {Object} opts
 * @param {string} opts.ventureName - Human-readable venture name
 * @param {string} opts.repoName - GitHub repo name (kebab-case)
 * @param {string} opts.repoPath - Local filesystem path to the venture repo
 * @param {string} [opts.description] - Venture description
 * @returns {string} Path to the generated CLAUDE.md
 */
export function generateVentureClaudeMd({ ventureName, repoName, repoPath, description }) {
  const content = `# CLAUDE.md - ${ventureName}

## Project Context
This is a venture repository managed by the LEO Protocol orchestrator.
- **Venture**: ${ventureName}
- **Repo**: rickfelix/${repoName}
- **Orchestrator**: EHG_Engineer (LEO Protocol)
${description ? `- **Description**: ${description}` : ''}

## Development Workflow
This venture follows the LEO Protocol workflow (LEAD -> PLAN -> EXEC).
- Strategic Directives target this repo via \`target_application: "${repoName}"\`
- Changes are tracked in the shared Supabase database
- PRs are created against the \`main\` branch

## Key Rules
- All changes must be tracked via SD or Quick-Fix workflow
- Pre-commit hooks enforce secret detection
- Code must pass CI checks before merge

## Related Repositories
- **EHG_Engineer**: Backend orchestrator and LEO Protocol
- **ehg**: Frontend application

---
*Generated by venture-claude-md-generator.js | LEO Protocol*
`;

  const outputPath = join(repoPath, 'CLAUDE.md');
  writeFileSync(outputPath, content);
  return outputPath;
}

export default { generateVentureClaudeMd };
