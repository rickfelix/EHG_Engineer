// SD-LEO-INFRA-PLAN-LINKAGE-BELT-001 (FR-1): tag-at-the-door linkage classifier.
//
// The roadmap-promotion door (source-adapters/roadmap-item.js) already links by
// construction via promoted_to_sd_key (SD-LEO-INFRA-PLAN-OF-RECORD-LINKAGE-001) and is
// NOT covered here. This module classifies the 6 remaining creation doors
// (feedback/qf/learn/plan/child/uat) plus the fence-lift door
// (lib/coordinator/clear-coordinator-review.js), so every belt admission carries a
// metadata.plan_linkage stamp: either a real wave/rung link, or an honest classified
// "none" — never a free-text guess.

const HARNESS_PREFIX = /^(SD-LEO-INFRA-|SD-MAN-INFRA-|SD-LEARN-FIX-|QF-)/;
const PRODUCT_PREFIX = /^SD-EHG-PRODUCT-/;

export const UNLINKED_REASONS = Object.freeze(['harness-upkeep', 'venture-ops', 'emergent-fix']);

/**
 * PURE: classify an unlinked SD's reason from its key/venture context.
 * Mirrors the SD-key-prefix band already applied by productPivotCompare()
 * in scripts/coordinator-backlog-rank.mjs, so the two stay consistent without
 * a cross-module import (that comparator lives in a CLI script, not a lib).
 * @param {string} sdKey
 * @param {string|null} ventureId
 * @returns {'harness-upkeep'|'venture-ops'|'emergent-fix'}
 */
export function classifyUnlinkedReason(sdKey, ventureId = null) {
  if (typeof sdKey === 'string' && HARNESS_PREFIX.test(sdKey)) return 'harness-upkeep';
  if ((typeof sdKey === 'string' && PRODUCT_PREFIX.test(sdKey)) || ventureId) return 'venture-ops';
  return 'emergent-fix';
}

function nowIso() {
  return new Date().toISOString();
}

function linked({ waveId = null, waveTitle = null, rung = null }, classifiedBy) {
  return {
    linked: true,
    wave_id: waveId,
    wave_title: waveTitle,
    rung,
    unlinked_reason: null,
    classified_at: nowIso(),
    classified_by: classifiedBy,
  };
}

function unlinked(reason, classifiedBy, extra = {}) {
  return {
    linked: false,
    wave_id: null,
    wave_title: null,
    rung: null,
    unlinked_reason: reason,
    classified_at: nowIso(),
    classified_by: classifiedBy,
    ...extra,
  };
}

/**
 * Classify plan-linkage for a newly-created SD (called at metadata-build time by
 * each non-roadmap-item source adapter, BEFORE createSD() inserts the row).
 *
 * @param {object} params
 * @param {string} params.sdKey - the SD's own key (already generated by the caller)
 * @param {string|null} [params.ventureId] - venture_id, if known at creation time
 * @param {object|null} [params.parentSd] - full parent SD row (child.js only; select('*') result)
 * @param {{waveId?: string, noWave?: string}|null} [params.waveDisposition] - the raw
 *   wave-disposition proposal (plan.js only, via overrides.waveDisposition) — the SAME
 *   existing waves-as-gates mechanism used for orchestrator creation
 *   (lib/roadmap/wave-disposition.js). Only validated/applied downstream when the
 *   resulting SD is an orchestrator; for non-orchestrator plans this is best-effort intent.
 * @returns {object} plan_linkage shape
 */
export function classifyPlanLinkage({ sdKey, ventureId = null, parentSd = null, waveDisposition = null }) {
  // 1) Child inherits a wave-linked parent's linkage verbatim.
  if (parentSd?.metadata?.plan_linkage?.linked === true) {
    const p = parentSd.metadata.plan_linkage;
    return linked({ waveId: p.wave_id, waveTitle: p.wave_title, rung: p.rung }, 'creation');
  }

  // 2) An explicit wave-disposition proposal (plan.js, waves-as-gates mechanism).
  if (waveDisposition && typeof waveDisposition === 'object') {
    if (typeof waveDisposition.waveId === 'string' && waveDisposition.waveId.trim()) {
      return linked({ waveId: waveDisposition.waveId.trim() }, 'creation');
    }
    if (typeof waveDisposition.noWave === 'string' && waveDisposition.noWave.trim()) {
      const reason = classifyUnlinkedReason(sdKey, ventureId ?? parentSd?.venture_id ?? null);
      return unlinked(reason, 'creation', { wave_disposition_reason: waveDisposition.noWave.trim() });
    }
  }

  // 3) Default: classify from sd_key prefix / venture context.
  const reason = classifyUnlinkedReason(sdKey, ventureId ?? parentSd?.venture_id ?? null);
  return unlinked(reason, 'creation');
}

export default { classifyPlanLinkage, classifyUnlinkedReason, UNLINKED_REASONS };
